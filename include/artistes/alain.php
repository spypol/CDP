    <span class="details-infos" id="details-name">Alain Tournay</span>
    <span class="details-infos">Crochet</span>


    
    <p>
Alain Tournay a commencé à prendre ses premiers cours de danse à St Leu la Forêt dans le val
d'Oise où il a grandi.Il se perfectionne ensuite dans les grands centre de danse de Paris dont
Harmonic et Paris centre. Après une carrière de danseur qui le conduira à danser dans divers
domaines : Le Music-hall (Lido de Paris), cinéma (Chatillez), publicité (Maaf), il se formera au
chant en comédie musicale avec Pierre-Yves Duchesne et en chant lyrique au conservatoire
d'Asnières avec Danielle Dinant. Il étudie également le théâtre auprès de différents professeurs
tels qu'Axelle Abadi, Jean Barney, Jean-Luc Moreau ou Fréderic Baptiste. La maîtrise de ces trois
disciplines l'amène logiquement à la comédie musicale. On a pu le voir dans «Hello Doly» en
tournée française, «Chantons sous la pluie» à l'Opéra de Liège, «Blanche Neige» aux Folies
Bergères où il était doublure du rôle de Gontran le majordome, la comédie musicale «Chicago»
où il a eu la chance de faire partie des rares français au sein de la troupe Canadienne. Mais aussi
«Il faut marier Maman» à l'Odéon de Marseille dans le rôle de Jean-Paul, «Victoria et son hussard»
dans le rôle de Ferry, «La route Fleurie» dans le rôle de Jean-Pierre au festival d'Aix les Bains et la
pièce «Pygmalion» au Studio Théâtre de Stains dans le rôle de Freddy Eynsford-Hill, rôle qu'il
reprendra l'année suivante dans la célèbre comédie musicale tirée de cette même pièce «My fair
lady» en tournée française.
Il a également fait partie du cast de «Grease» au théâtre Comédia en doublure de Teenangel et
Vince Fontaine ainsi que la comédie musicale «Zorro» produite par Stage Entertainment aux
Follies Bergères en doublure de Zorro et Ramon.Il fait partie du projet «Le baiser de la femme
araignée» mis en scène par Jean-Luc Revol, Il reprend son rôle
de Jean-Pierre dans «La route Fleurie» à l'Odéon de Marseille en 2012 ainsi que le rôle de
Ferdinand dans «4 jours à Paris» l'année suivante. En 2012, Il interprète également le rôle de Boni
dans «Princesse Czardas» à la bourse du travail à Lyon et le rôle du capitaine Harris dans
«Passionement» l'année suivante. Par le biais du conservatoire d'Asnières, il s'essaie également à
la mise en scène et à la chorégraphie sur le spectacle «Offenbach Follies» dans lequel il interprète
aussi le Brésilien.Il est doublure de trois rôle principaux, Gaston, Big Ben et lumière dans «La Belle
et la Bête» au théâtre Mogador. Aujourd’hui, il tient le rôle de Crochet dans «Peter Pan et les
Pirates».
    </p>
