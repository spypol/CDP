    <span class="details-infos" id="details-name">Raphaelle Arnaud</span>
    <span class="details-infos">Doublure Reine / Chenille</span>


    
    <p>
Diplômée de l’Académie Internationale de Comédie Musicale (AICOM) en 2011, Raphaëlle s’est
produite depuis dans de nombreux spectacles tels que «1939» (Théâtre du Gymnase avec Lara
Fabian), «Paris-Broadway» (Espace Cardin), «Anne le musical» de J-P Hadida (Espace Cardin et
Théâtre du Gymnase), «La Route Fleurie, Clémenceau» (Opéra de Reims, Opéra de Massy, Théâtre
des Jacobins à Dinan, etc.) et «Gilles de Rais l’Ombre et la lumière» (Château de Tiffauges dans
lequel elle interprète le rôle de Jeanne d’Arc) de Jaques Duparc.
Elle a découvert aussi les joies des spectacles jeune public avec «La belle au bois dormant, que
veillent les fées» au Théâtre du Marsoulan, «Kid Manoir 1» et «Kid Manoir 2» au Palais des Glaces.
De 2012 à 2014, elle a joué avec bonheur dans la troupe de «Mamma Mia !», N°1 mondial des
comédies musicales, en tournée dans les Zénith de France et dans laquelle elle était doublure du
premier rôle féminin.
Parallèlement, elle continue de se former à la Danse Sportive, son premier amour, et développe
son répertoire en variant les plaisirs lors de concerts Gospel et Disco (Sporting Monte-Carlo de
Monaco, Disneyland Paris, Casino d’Enghien, Place de la République à Paris pour le 14 juillet,
etc.).
    </p>
