    <span class="details-infos" id="details-name">Sarah Perahim</span>
    <span class="details-infos">Assistante mise en scène</span>
    <span class="details-infos">Assistante de production</span>
    
    <p>
        Formée dès l'âge de 6 ans au Conservatoire de Paris en chant, piano et danse classique, c'est après le bac que Sarah se lance dans le théâtre. Elle fera ses premiers pas sur scène avec « Les larmes amères de Petra Von Kant » de R.W. Fassbinder au théâtre du Marais et à l'Aktéon en 2010, ainsi qu'au théâtre du Marsoulan dans « L'envol 1946 » en 2012.
Elle tiendra les rôles principaux dans « Hair » et « West Side story » au sein du Studio International, afin d'étendre ses compétences classiques à la comédie musicale. Parallèlement, Sarah a tourné dans une quinzaine de courts métrages ainsi que quelques webs-séries, et continue d'écrire et de composer, essayant de multiplier au maximum ses compétences artistiques. En 2013, Sarah fait la mise en scène d'une première partie de spectacle, et découvre un immense goût pour la mise en scène ainsi que pour le coaching d'acteur. Actuellement sur scène dans "Les demoiselles d'Avignon", joué à Rennes, elle rejoint la troupe d' « Alice » avec joie, pour étendre ses connaissances en production, direction de chœurs, et assistanat de mise en scène.
    </p>
