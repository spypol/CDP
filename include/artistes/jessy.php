    <span class="details-infos" id="details-name">Jessy Roussel</span>
    <span class="details-infos">Mouche</span>


    
    <p>
Jessy commence sa carrière en chantant dans différentes comédies musicales telles que «Les
1001 vies d'Ali Baba» (tournée Zénith de France) ou «La petite boutique des horreurs» au Théâtre
Déjazet.
En 2004, elle part vivre au Japon. Elle est co-animatrice sur la chaîne de télé NHK et décroche sa
propre émission de voyage en japonais pour le «guide Michelin».
Elle fait une apparition dans le long métrage à succès «Nodame Cantabile 2».
En 2010, elle rentre en France et tient le rôle principal dans la comédie musicale pour enfants
«Les aventures de Rosalie la petite souris» au théâtre du Gymnase.
Actuellement, elle fait partie du groupe «Erik Sitbon and the Ghost band» pour lequel elle écrit.
Aujourd’hui, elle montre l’ampleur de ses talents dans le rôle de Mouche.
