    <span class="details-infos" id="details-name">Emma Brazeilles</span>
    <span class="details-infos">Wendy</span>


    
    <p>
Emma démarre la danse classique à 5 ans, puis se forme au modern-jazz et à la danse
contemporaine. Passionnée de chant depuis l'âge de 4 ans, et faisant partie d'une petite
compagnie théâtrale dans sa région, elle réalise pendant ses études universitaires que sa place
est ailleurs... Elle part donc s'installer à Paris pour suivre une formation artistique complète.
Elle intègre l'Ecole de Comédie Musicale de Paris en 2010 et se voit proposer au bout d'un an, le
rôle de Camille, l'une des petites filles modèles (ou presque !) et du loup dans la comédie
musicale pour enfants «Les Malheurs de Sophie» de Virginie Aguzzoli.
Elle participe en parallèle à l'émission «Sing off 100% vocal» sur France 2 où elle chante
exclusivement a cappella avec son groupe qui ira jusqu'en demi-finale.
Elle décroche ensuite le rôle de la petite fée Lalïa dans le spectacle musical jeune public
«Symphonia et la Magie de la Nature» de Cécile Jabinet qui se jouera au Festival d'Avignon 2012
et en tournée.
Puis viennent les comédies musicales «Kid Manoir» et «Robin des Bois, la légende… ou presque»
de Guillaume Beaujolais et Fred Colas où elle interprète respectivement les rôles de Gwendy,
Jonquille et de la Princesse Marianne.
Actuellement, Emma tient le rôle de Blanche-neige dans le musical «Blanche Neige et Moi» au
Théâtre La Boussole à Paris, en parallèle de son rôle de Wendy.
    </p>
