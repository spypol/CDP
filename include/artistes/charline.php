    <p>
   Encouragée par ses parents, Charline commence la danse classique, à l’âge de 5 ans. Intéressée
et enthousiaste, elle gagne vite en technique, et trouve un réel plaisir à danser divers styles de
danses, étoffant ainsi son vocabulaire gestuel.
Au lycée elle entre dans une des meilleures sections TMD (technique musique et danse) de
France, où elle intègre le conservatoire de danse classique de Montpellier.
À la fin de sa scolarité elle obtient le Bac TMD, l'EAT danse classique et la médaille de fin d'étude
du conservatoire de danse de Montpellier.
Se passionnant secrètement depuis toujours pour le chant, la musique et le théâtre, elle est
diplômée en Juin 2014 de «l'ECM 3 arts» de Paris (école de comédie musicale) où elle apprend
l'art du théâtre musical.
Durant sa formation elle participe à divers spectacles dont «La fête des vendanges» à
Montmartre en 2011, le «One Woman show» de Karine Dubernet au Point Virgule. Elle est
également l'assistante chorégraphique de Linda Faoro sur les spectacles de fin d'année 2012 et
2013 de l'ECM de Paris.
Aujourd'hui Charline est une jeune artiste polyvalente qui joue pour la première fois sur les
planche du Théâtre Clavel, dans le rôle de Peter Pan.
    </p>
