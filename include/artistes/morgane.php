    <span class="details-infos" id="details-name">Morgane L'Hostis Parisot</span>
    <span class="details-infos">Alice</span>


    
    <p>
   Morgane commence à prendre des cours de chant et de danse classique dès l'âge de 5 ans. Puis elle pratique la gymnastique rythmique en compétition intensive pendant cinq années consécutives, tout en travaillant le chant gospel en cours particuliers. Dès son entrée au collège, elle s'inscrit au Conservatoire d'Agglomération d'Evry Centre Essonne, et suit un triple cursus diplômant en danses classique, modern jazz et contemporaine. Durant ses quatre années au collège, Morgane participe à l'atelier orchestre et chorale en tant que pianiste et chanteuse, et prend ainsi conscience de son plaisir sur scène.  En 2013, elle débute son apprentissage du chant lyrique. Durant ses vacances, Morgane anime des stages pour adolescents de Création Chorégraphique. C'est en été 2014, à l'âge de 15 ans, qu'elle découvre sa passion pour la comédie musicale en participant à un stage au Studio International des Arts de la Scène de Vanina Mareschal, avec Hervé Lewandowski. Elle passe sa première audition chez Compote de Prod à la rentrée scolaire, tout en continuant sa scolarité au lycée, ses cours de danse, chant et formation musicale au conservatoire, ainsi qu'une formation pré-professionnelle au Studio.
    </p>
