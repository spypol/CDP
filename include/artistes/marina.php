    <span class="details-infos" id="details-name">Marina Pangos</span>
    <span class="details-infos">Metteur en scène</span>
    
    <p>
        Comédienne et chanteuse, Marina côtoie la scène dès son plus jeune âge. La maturité et la sensibilité vocale dont elle fait preuve séduit ses professeurs qui la confortent dans ses aspirations artistiques. Après une formation classique (piano, solfège, chant lyrique) et un voyage initiatique (gospel et jazz) aux Etats-Unis, la voilà qui débarque sur la capital pour poursuivre des formations pluridisciplinaires (théâtre, chant et danse). Depuis 2011, elle apparaît dans diverses productions de spectacles musicaux, dont elle assure également la direction vocale pour certains d'entre eux, elle défend "Les Aventures du Prince Bonbec" de Martin Leloup au Festival Off d'Avignon, et obtient son premier rôle devant la caméra dans "L'Enfant Nuage" de Rudy Etienne en 2013. Elle signera sa première mise en scène pour le spectacle musical « Alice » au printemps prochain.
    </p>
