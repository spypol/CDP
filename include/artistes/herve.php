    <span class="details-infos" id="details-name">Hervé Lewandowski</span>
    <span class="details-infos">Le Chapelier Fou et Lewis Carroll</span>
    <span class="details-infos"><a href="http://www.hervelewandowski.com/" title="Site Hervé">hervelewandowski.com</a></span>

    
    <p>
    Formé au théâtre, à la danse et au chant dès l’âge de 14 ans, Hervé a déjà un long parcours artistique derrière lui. Il effectue dès son plus jeune âge des tournées à travers toute l’Europe avec les groupes Evasion et Anonym’, et est ensuite Formé à l’Ecole Supérieure du Spectacle, aux Studios Alice Dona et à l’académie internationale de Danse. Depuis 1998, Hervé a participé à de très nombreux spectacles : « The Rocky Horror Show » au théâtre de la Plaine, « Hair » qu’il met en scène en 2001, « La petite boutique des horreurs » au Théâtre Dejazet, « Rent » de Jonathan Larson en Suisse, « Tintin le Temple du Soleil » qui triomphera en Belgique, et bien d’autres encore. Ces quinze dernières années,  il a joué dans de nombreuses comédies musicales. Dernièrement, il s’est illustré dans « Le Roi Lion » au théâtre Mogador où il joua les rôles de Timon, Zazu et Ed. Hervé a également participé à «  Robin des Bois » au théâtre du Temple, « Mamma Mia ! » à Mogador, « Raiponce », et est actuellement sur les planches pour « L’Ile aux Trésors », au Théâtre de la Madeleine. 
    </p>
