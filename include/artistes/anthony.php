    <p>
   Depuis son plus jeune âge, Anthony voue une passion démesuré pour le chant, la musique, le spectacle... L'univers artistique l'appel. Auteur compositeur interprète, c'est en 2008 qu'il s'installe à Paris, et qu'il commence à apprendre "sur le tas" les rouages du métier. De formation il n'a eu que le don des autres, et les rues de Montmartre.  Comédies musicales (Le prince et le pauvre, piaf je t'aime, la petite sirène, la sorcière éphémère, la fée Sidonie...), Cabarets (La nouvelle Distilerie, Le Beaulieu, le Melrose...), Théâtre... Vous pouvez aussi le croiser dans de nombreux endroits sur Paris (Le Chansonnier, Le Golden pat, La Pomponette...) où il se produit en tant que chanteur très régulièrement. Il est actuellement en préparation de son album, et de nombreux autres projets.
