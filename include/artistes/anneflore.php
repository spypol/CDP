    <span class="details-infos">Doublure Alice</span>


    
    <p>
   Dès l'âge de ces 7 ans, Anne-Flore s'initie à la musique en apprenant le solfège et la harpe de manière traditionnelle au conservatoire national de Saint Maur des fossés. Puis, elle intègre la chorale du conservatoire pendant deux ans avant de se lancer en 2007 dans un chœur d'enfants semi-professionnel ; le chœur d'enfants Sotto Voce. Chaque année le chœur monte un opéra ou un spectacle musical à l’Opéra Bastille ou au Théâtre du Châtelet ce qui a permis à Anne-Flore de suivre une formation poussée dans le chant et le théâtre. Anne-Flore a notamment eu l'occasion d'être soliste dans plusieurs salles parisiennes telles que la Salle Gaveau, l’Hôtel de Ville de Paris, l’église Américaine et le Théâtre du Châtelet.

Ces expériences ont permis à Anne-Flore de développer une passion pour le monde artistique et les comédies musicales, et lui ont fait découvrir un goût certain pour la scène. 
    </p>
