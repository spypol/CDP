    <p>Bercée dans un univers musical très varié, Julie Lemas se forme très tôt aux différents domaines artistiques : solfège, piano, danse modern jazz. Sa passion pour le théâtre, elle la découvre aux côtés de ses parents qui ont monté une troupe dans le sud de l'Essonne. Mais ce n'est qu'à l'âge de 16 ans que Julie ose intégrer un cours de théâtre pour ainsi apprendre puis se perfectionner au métier de comédien. En 2002, sa rencontre avec Philippe Padovani, son professeur de chant (lyrique et moderne), marque un tournant dans sa vie artistique. Au travers de différentes productions musicales, Julie peut enfin mêler toute sa palette artistique et découvre le bonheur d'être sur scène et d'avoir un public.
Alors qu'elle se destine au métier de Professeur des écoles, Julie décide de tout arrêter pour laisser une chance à ses rêves. A la rentrée 2007, elle intègre l'Académie Internationale de Comédie Musicale pour perfectionner son talent.
 
C'est en 2008 que Julie sort de l'ombre en décrochant le rôle de Chloé dans la comédie musicale "Je m'voyais déjà" écrite par Laurent Ruquier et mise en scène par Alain Sachs. Elle foule ainsi pour la première fois les planches d'un théâtre parisien en interprétant les plus grandes chansons de Charles Aznavour.

En 2010, elle intègre l'équipe de Jacques Duparc entant qu'ensemble dans l'opérette de Francis Lopez, "La route fleurie" et plus tard, dans "L'Auberge du cheval blanc" de Samuel Beckett.

Julie rencontre l'équipe de production "Compote de Prod" en 2011. Avec "Souviens-toi Pan", ils lui proposent de faire du moussaillon Mouche une jeune femme hardie et futée. Un rôle de caractère qui fait apparaître une nouvelle facette de sa personnalité au public.

L'année 2014 marquera le retour de Julie sur les planches. Après un retour dans la folle "Auberge du cheval" en février, elle retrouvera l'équipe de "Compote de Prod" pour sa nouvelle production "Alice, la comédie musicale". A suivre au théâtre Claver dès le 1er Avril.
</p>
