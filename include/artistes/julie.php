    <span class="details-infos">Souviens-toi Pan !</span>
    <p>Julie se forme très tôt aux différents domaines artistiques : solfège, piano, danse modern jazz. Très jeune, elle se découvre 

également une passion pour le théâtre aux côtés de ses parents qui ont monté une troupe en Essonne. Mais ce n'est qu'à 

l'âge de 16 ans que Julie ose intégrer un cours de théâtre pour apprendre le métier de comédien. En 2002, sa rencontre avec 

Philippe Padovani, professeur de chant, marque un tournant dans sa vie artistique. Au travers de différentes productions 

musicales, Julie découvre le bonheur d'être sur scène et d'avoir un public. A la rentrée 2007, elle intègre l'Académie 

Internationale de Comédie Musicale, et c’est en 2008 que Julie sort de l'ombre en décrochant le rôle de Chloé dans la comédie 

musicale "Je m'voyais déjà", écrite par Laurent Ruquier et mise en scène par Alain Sachs. Julie foule ainsi pour la première 

fois les planches d'un théâtre parisien en interprétant les plus grandes chansons de Charles Aznavour. On la retrouve en 2010 

dans l'opérette de Francis Lopez, "La route fleurie" et dans "L'Auberge du cheval blanc" de Samuel Beckett. Julie rencontre 

l'équipe de production "Compote de Prod" en 2011. Dans "Souviens-toi Pan », elle incarne Mouche, une moussaillon hardie et 

futée. L’année 2014 marque le retour d’Alice sur les planches. Après un retour dans la folle "Auberge du cheval" en février, elle 

incarnera la Reine de Cœur dans "Alice, la comédie musicale".
</p>
